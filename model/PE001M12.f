      TAUMCL=0.
      TAUWC(L)=0.
      TAUIC(L)=0.
      SIZEWC(L)=CSIZE(I,J,L,1)
      SIZEIC(L)=CSIZE(I,J,L,1)
         TOTCLD(L)=0.

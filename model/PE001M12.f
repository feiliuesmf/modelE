         CJ(J,47)=CJ(J,47)+CRUN4
         AJ(J,48)=AJ(J,48)+AERUN4
         CJ(J,48)=CJ(J,48)+CERUN4
         BJ(J,49)=BJ(J,49)+BWTR1
         BJ(J,50)=BJ(J,50)+BACE1

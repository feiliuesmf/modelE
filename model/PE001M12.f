      IF(CLDSAV(I,J,L).LT.1.)
     *  QL(L)=(Q(I,J,L)-QSS*CLDSAV(I,J,L))/(1.-CLDSAV(I,J,L))
      TL(L)=T(I,J,L)*PK(L,I,J)

      CALL SUBSID(SM,SXM,SYM,SZM,SXXM,SYYM,SZZM,SXYM,SYZM,SZXM,
     *     ML,.FALSE.,CM,LMIN,LMAX,-1)
      DO L = LMIN,LMAX
          SM(L) =   SM(L) +   DSM(L)
         SXM(L) =  SXM(L) +  DSXM(L)

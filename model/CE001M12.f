  220 Q1=0.
      IF(WMX(L).GT.0.) PREP(L)=WMX(L)*BYDTCN
      ER(L)=(1.-RH(L))*LHX*PREBAR(L+1)*GRAV*BYAM(L)
      IF(PREICE(L+1).GT.0..AND.TL(L).LT.TF)
     *  ER(L)=(1.-RHI)*LHX*PREBAR(L+1)*GRAV*BYAM(L)
      IF(ER(L).LT.0.) ER(L)=0.
      QHEAT(L)=-CAREA(L)*ER(L)+Q1
      WMNEW=0.
  230 CONTINUE

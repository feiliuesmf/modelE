c      IF (WMX(L).gt.1d-20) FPR=PREP(L)*DTCNDS/WMX(L)          ! CLW->P
c      FER=0.
c      IF (PREBAR(L+1).gt.1d-20) FER=CAREA(L)*ER(L)*AIRM(L)/(

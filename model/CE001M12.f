  530 PRHEAT=CDHEAT(L)+SLH*PRCP
      PRCP=PRCP+COND(L)
  540 CONTINUE
C****
      IF(PRCP.GT.0.) CLDMC(I,J,1)=CLDMC(I,J,1)+CCM(LMIN)*BYAM(LMIN+1)
      PRCPMC=PRCPMC+PRCP
c         IF(QWRITE) THEN

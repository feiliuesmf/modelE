      MODULE CLOUDS
!@sum  CLOUDS moist convction and large-scale clouds and precipitation
!@auth M.S.Yao/T. Del Genio (modifications by Gavin Schmidt)
!@ver  1.0 (taken from CB265)
!@uses E001M12,SOMTQ,RANDOM
!@cont MSTCNV,CONDSE,CTMIX,QSAT

C**** adapted to R99E+ radiation
C**** Uses constant pressure coordinates for L=LS1-LM.
C**** Uses QUS for subsidence + keep tracks of moments
C**** + change to CTEI calc (incl. tracer mom. mixing subr. CTMIX)
C**** Subsid only works on non-plume portion of column (properly!)

      USE CONSTANT, only : rgas,grav,lhe,lhs,lhm,kapa,sha,bysha
      USE E001M12_COM
      USE SOMTQ_COM
      USE RANDOM
      USE GEOM
      USE DAGCOM, only : aj,bj,cj,dj,aij,ajl,ail,adaily,jreg
      
      IMPLICIT NONE

      REAL*8, SAVE :: SD_CLOUDS,PREC,TPREC,TAUSS,TAUMC,UC,VC,PRECSS
      DIMENSION SD_CLOUDS(IM,JM,LM),PREC(IM,JM),
     *  TPREC(IM,JM),TAUSS(IM,JM,LM),TAUMC(IM,JM,LM),
     *  UC(IM,JM,LM),VC(IM,JM,LM),PRECSS(IM,JM)

      REAL*8, SAVE :: TTOLD,QTOLD,PTOLD,WM,SVLHX,RHSAV,PBLTOP,SVWMX
     *     ,SVLAT,CLDSAV
      DIMENSION TTOLD(IM,JM,LM),QTOLD(IM,JM,LM),
     *  PTOLD(IM,JM),WM(IM,JM,LM),SVLHX(IM,JM,LM),
     *  RHSAV(IM,JM,LM),PBLTOP(IM,JM),
     *  SVWMX(IM,JM,LM),SVLAT(IM,JM,LM),CLDSAV(IM,JM,LM)

      REAL*8, SAVE :: CLDSS,CLDMC,CSIZE
      DIMENSION CLDSS(IM,JM,LM),CLDMC(IM,JM,LM),CSIZE(IM,JM,LM,2)

      REAL*8, SAVE :: GZ,PRECNV,VSUB
      DIMENSION GZ(IM,JM,LM),PRECNV(IM,JM,LM+1),VSUB(IM,JM,LM)

      REAL*8, PARAMETER :: TF=273.16d0   !@param TF freezing point (K)
      REAL*8, PARAMETER :: BYTF=1./TF    !@param BYTF recip. of TF
      REAL*8, PARAMETER :: TI=233.16d0   !@param TI pure ice limit
      REAL*8, PARAMETER :: RVAP=461.5d0  !@param RVAP 
      REAL*8, PARAMETER :: RECIP3=1.d0/3.d0 !@param RECIP3 = 1/3
      REAL*8, PARAMETER :: WMU=.25       !@param WMU 
      REAL*8, PARAMETER :: WMUL=.5       !@param WMUL
      REAL*8, PARAMETER :: WMUI=.1d0     !@param WMUI
      REAL*8, PARAMETER :: BRCLD=.2d0    !@param BRCLD

      REAL*8, SAVE :: DTCNDS,BYGRAV,PK1000,BYBR,SLHE,SLHS
     *     ,BYDTCN,AXCONS,BXCONS,DTPERD,AGESNX,DQDTX

      REAL*8, SAVE :: AQ1,AQ2,CLDDEP,CLDSLW
      DIMENSION AQ1(IM,JM,LM),AQ2(IM,JM,LM),CLDDEP(IM,JM),
     *  CLDSLW(IM,JM)

      PRIVATE QSAT,CTMIX

      CONTAINS

      SUBROUTINE MSTCNV
!@sum  MSTCNV moist convective processes (incl. precip and convective clouds)
!@auth M.S.Yao/T. Del Genio (modifications by Gavin Schmidt)
!@ver  1.0 (taken from CB265)
!@calls SUBSID,QSAT,THBAR  !,EXPBYK 
      IMPLICIT NONE

      REAL*8 LHX,MPLUME,MCLOUD,MPMAX,MPO
      INTEGER, DIMENSION(IM) :: IDI,IDJ    !@var ID 
      REAL*8, DIMENSION(IM) :: UMP,VMP,UMDN,VMDN,UMPO,VMPO  !@var
      REAL*8, DIMENSION(IM) :: RA !@var
      REAL*8, DIMENSION(IM,LM) :: UM,VM,U_0,V_0,DUM,DVM !@var
      REAL*8, DIMENSION(0:LM) :: CM     !@var CM
      REAL*8, DIMENSION(LM+1) :: PLE    !@var PLE
!@var Miscellaneous vertical arrays
      REAL*8, DIMENSION(LM) ::
     * PL,PLK,DM,AIRM,COND,CDHEAT,CCM,ETAL,AJ8,AJ13,AJ50,AJ51,AJ52,AJ57,
     *       SM1,QM1,DMR,BYAM,ML,SMT,QMT,TL,TPSAV,SVTP
!@var SM,QM,DQM,DSM,DQMR,DSMR Vertical profiles of T/Q and changes
      REAL*8, DIMENSION(LM) ::
     * SM,SXM,SYM,SZM,SXXM,SXYM,SYYM,SYZM,SZZM,SZXM,
     * QM,QXM,QYM,QZM,QXXM,QXYM,QYYM,QYZM,QZZM,QZXM,
     * SMOLD,SXMOLD,SYMOLD,SZMOLD,SXXMOLD,SXYMOLD,SYYMOLD,SYZMOLD,
     *       SZZMOLD,SZXMOLD,
     * QMOLD,QXMOLD,QYMOLD,QZMOLD,QXXMOLD,QXYMOLD,QYYMOLD,QYZMOLD,
     *       QZZMOLD,QZXMOLD,
     * DQM,DQXM,DQYM,DQZM,DQXXM,DQXYM,DQYYM,DQYZM,DQZZM,DQZXM,
     * DSM,DSXM,DSYM,DSZM,DSXXM,DSXYM,DSYYM,DSYZM,DSZZM,DSZXM,
     * DQMR,DQXMR,DQYMR,DQZMR,DQXXMR,DQXYMR,DQYYMR,DQYZMR,DQZZMR,DQZXMR,
     * DSMR,DSXMR,DSYMR,DSZMR,DSXXMR,DSXYMR,DSYYMR,DSYZMR,DSZZMR,DSZXMR

      COMMON/WORK3/
     * SM,SXM,SYM,SZM,SXXM,SXYM,SYYM,SYZM,SZZM,SZXM,
     * QM,QXM,QYM,QZM,QXXM,QXYM,QYYM,QYZM,QZZM,QZXM,
     * SMOLD,SXMOLD,SYMOLD,SZMOLD,SXXMOLD,SXYMOLD,SYYMOLD,SYZMOLD,
     *     SZZMOLD,SZXMOLD,
     * QMOLD,QXMOLD,QYMOLD,QZMOLD,QXXMOLD,QXYMOLD,QYYMOLD,QYZMOLD,
     *     QZZMOLD,QZXMOLD,
     * DQM,DQXM,DQYM,DQZM,DQXXM,DQXYM,DQYYM,DQYZM,DQZZM,DQZXM,
     * DSM,DSXM,DSYM,DSZM,DSXXM,DSXYM,DSYYM,DSYZM,DSZZM,DSZXM,
     * DQMR,DQXMR,DQYMR,DQZMR,DQXXMR,DQXYMR,DQYYMR,DQYZMR,DQZZMR,DQZXMR,
     * DSMR,DSXMR,DSYMR,DSZMR,DSXXMR,DSXYMR,DSYYMR,DSYZMR,DSZZMR,DSZXMR
      REAL*8 QMALL(10*LM),SMALL(10*LM),QMOLDALL(10*LM),SMOLDALL(10*LM),
     *  DMALL(40*LM)
      EQUIVALENCE (QMALL,QM),(SMALL,SM),(QMOLDALL,QMOLD),
     *  (SMOLDALL,SMOLD),(DMALL,DQM)

      INTEGER LDRAFT,LEVAP,LMAX,LMCMIN,LMCMAX,LMIN,MCCONT,MAXLVL
     *     ,MINLVL,ITER,IC,LFRZ
      REAL*8 PLAND,POICE,POCEAN,PLICE,PEARTH,TERM1,FMP0,SMO1
     *     ,QMO1,SMO2,QMO2,SDN,QDN,SUP,QUP,SEDGE,QEDGE,WMDN,WMUP,SVDN
     *     ,SVUP,WMEDG,SVEDG,SLH,DMSE,FPLUME,DFP,FMP2,FRAT1,FRAT2,SMN1
     *     ,QMN1,SMN2,QMN2,SMP,QMP,TP,QSATMP,GAMA,DQSUM,FEVAP,TNX,QNX
     *     ,QSATC,DQ,DMSE1,FTYPE,SXMP,SYMP,SXXMP,SXYMP,SYYMP,QXMP,QYMP
     *     ,QXXMP,QXYMP,QYYMP,CDHSUM,CDHDRT,ETADN,EVPSUM,DDRAFT,DELTA,
     *     CONSUM,ALPHA,PRHEAT,BETA,CDHM,CDHSUM,CLDM,CLDREF,CONSUM,DQEVP
     *     ,DQRAT,EPLUME,ETADN,ETAL1,EVPSUM,FCDH
     *     ,FCDH1,FCDH1,FCLD,FCLOUD,FDDL,FDDP,FENTR,FENTRA,FEVAP,FLEFT
     *     ,FQCOND,FSEVP,FSSUM,HCNDMC,HEAT1
     *     ,PIJ,PRCP,PRCPMC,PRHEAT
     *     ,QMDN,QMIX,QMPMAX,QMPT,QNX,QSATC,QSATMP,QXMDN
     *     ,QXMPMAX,QXXMDN,QXXMPMAX,QXYMDN
     *     ,QXYMP,QXYMPMAX,QYMDN,QYMPMAX,QYYMDN
     *     ,QYYMP,QYYMPMAX,RCLDE,SLH,SMDN,SMIX,SMPMAX,SMPT,SUMAJ
     *     ,SUMDP,SXMDN,SXMPMAX,SXXMDN,SXXMPMAX,SXYMDN
     *     ,SXYMPMAX,SYMDN,SYMPMAX,SYYMDN,SYYMPMAX
     *     ,TEM,TOLD,TOLD1,WCONST,WMSUM,WORK,WTEM,SMPO,QMPO,TEMWM
     *     ,RCLD

      LOGICAL QWRITE,MC1

      REAL*8,  PARAMETER :: CK1 = 1.       !@param CK1  ???
      REAL*8,  PARAMETER :: ENTCON = .2d0  !@param ENTCON  ???
      REAL*8,  PARAMETER :: DELTX=.608d0   !@param DELTX ???
      LOGICAL, PARAMETER :: MCSSC=.TRUE.  !@param MCSSC  TRUE IF NEW CONDSE USED
      INTEGER, PARAMETER :: JEQ = JM/2 + 1 !@param JEQ  N. equatorial gridpoint
      REAL*8,  SAVE :: BYDSIG(LM),XMASS   ! ,PKS(LM)
      INTEGER, SAVE :: IFIRST = 1

      INTEGER I,J,K,L,N  !@var I,J,K,L,N loop variables
      INTEGER IHOUR,IMAX,ITYPE,KMAX,IM1,JR,KR

      REAL*8 THBAR   !,EXPBYK
C****
      IDACC(1)=IDACC(1)+1
      IF(IFIRST.EQ.1) THEN
      DTCNDS=NCNDS*DT
      BYDTCN=1./DTCNDS
c      SHA=RGAS/KAPA
c      BYSHA=1./SHA
      BYGRAV=1./GRAV
      BXCONS=.622d0/RGAS
      AXCONS=LOG(6.1071D0)
      XMASS=0.1d0*DTCNDS*GRAV
      PK1000=1000.**KAPA
      BYBR=((1.-BRCLD)*(1.-2.*BRCLD))**RECIP3
      SLHE=LHE*BYSHA
      SLHS=LHS*BYSHA
c      DO 42 L=LS1,LM
c   42 PKS(L)=((PSF-PTOP)*SIG(L)+PTOP)**KAPA
      DO L=1,LM
        BYDSIG(L)=1./DSIG(L)
      END DO
      DQDTX=.622d0*LHE/RGAS
      DTPERD=DTCNDS/SDAY
      AGESNX=1.-DTPERD/50.
      IFIRST=0
      END IF

C**** SAVE UC AND VC, AND ZERO OUT CLDSS AND CLDMC
   70 DO 75 L=1,LM
      DO 75 J=1,JM
      DO 75 I=1,IM
      UC(I,J,L)=U(I,J,L)
      VC(I,J,L)=V(I,J,L)
         AQ1(I,J,L)=0.
         AQ2(I,J,L)=0.
      TAUSS(I,J,L)=0.
      TAUMC(I,J,L)=0.
      SVWMX(I,J,L)=0.
      SVLAT(I,J,L)=0.
      PRECNV(I,J,L)=0.
      VSUB(I,J,L)=0.
      CSIZE(I,J,L,1)=10.  !  used for stem of moist convective clouds
c      CSIZE(I,J,L,2)=0.   !  initialisation?
      CLDMC(I,J,L)=0.
   75 CLDSS(I,J,L)=0.
C**** SET PRECIPITATION AND LATENT HEAT
      DO 78 J=1,JM
      DO 78 I=1,IM
      CLDDEP(I,J)=0.
      CLDSLW(I,J)=0.
      PRECNV(I,J,LM+1)=0.
      PREC(I,J)=0.
   78 TPREC(I,J)=T(I,J,1)*PK(1,I,J)-TF
         IHOUR=1.5+TOFDAY
C****
C**** MAIN J LOOP
C****
      DO 810 J=1,JM

      KMAX=KMAXJ(J)
      IMAX=IMAXJ(J)
C****
C**** MAIN I LOOP
C****
  110 IM1=IM
      DO 800 I=1,IMAX
c         QWRITE=.FALSE.
         JR=JREG(I,J)
C****
C**** SET UP VERTICAL ARRAYS, OMITTING THE J AND I SUBSCRIPTS
C****
      PRCPMC=0.
      PLAND=FLAND(I,J)
      POICE=ODATA(I,J,2)*(1.-PLAND)
      POCEAN=(1.-PLAND)-POICE
c      PLICE=FLICE(I,J)     !*PLAND
      PEARTH=FEARTH(I,J)   !PLAND-PLICE
      DO K=1,KMAX
         RA(K)=RAJ(K,J)
         IDI(K)=IDIJ(K,I,J)
         IDJ(K)=IDJJ(K,J)
      ENDDO
C**** PRESSURES, AND PRESSURE TO THE KAPA
      PIJ=P(I,J)
      DO 150 L=1,LM
      IF(L.EQ.LS1) PIJ=PSF-PTOP

      PL(L) =PMID(L,I,J)
      PLE(L)=PEDN(L,I,J)
      PLK(L)=PK(L,I,J)
      AIRM(L)=PIJ*DSIG(L)
      BYAM(L)=1./AIRM(L)
      IF(L.LE.LM-2) ETAL(L+1)=.5*ENTCON*(GZ(I,J,L+2)-GZ(I,J,L))*
     *     1.d-3*BYGRAV
C**** TEMPERATURES
      SM(L)  =T(I,J,L)*AIRM(L)
      SXM(L) =TX(I,J,L)*AIRM(L)
      SYM(L) =TY(I,J,L)*AIRM(L)
      SZM(L) =TZ(I,J,L)*AIRM(L)
      SXXM(L)=TXX(I,J,L)*AIRM(L)
      SYYM(L)=TYY(I,J,L)*AIRM(L)
      SZZM(L)=TZZ(I,J,L)*AIRM(L)
      SXYM(L)=TXY(I,J,L)*AIRM(L)
      SYZM(L)=TYZ(I,J,L)*AIRM(L)
      SZXM(L)=TZX(I,J,L)*AIRM(L)
      TL(L)=T(I,J,L)*PLK(L)
      SVTP(L)=0.
         AJ13(L)=0.
         AJ50(L)=0.
         AJ51(L)=0.
         AJ52(L)=0.
         AJ57(L)=0.
C**** MOISTURE (SPECIFIC HUMIDITY)
      QM(L)  =Q(I,J,L)*AIRM(L)
      QXM(L) =QX(I,J,L)*AIRM(L)
      QYM(L) =QY(I,J,L)*AIRM(L)
      QZM(L) =QZ(I,J,L)*AIRM(L)
      QXXM(L)=QXX(I,J,L)*AIRM(L)
      QYYM(L)=QYY(I,J,L)*AIRM(L)
      QZZM(L)=QZZ(I,J,L)*AIRM(L)
      QXYM(L)=QXY(I,J,L)*AIRM(L)
      QYZM(L)=QYZ(I,J,L)*AIRM(L)
      QZXM(L)=QZX(I,J,L)*AIRM(L)
c      QL(L)=Q(I,J,L)
         AJ8(L)=0.
      SM1(L)=SM(L)
      QM1(L)=QM(L)
  150 CONTINUE
C**** SURROUNDING WINDS
      DO L=1,LM
         DO K=1,KMAX
            U_0(K,L) = UC(IDI(K),IDJ(K),L)
            V_0(K,L) = VC(IDI(K),IDJ(K),L)
            UM(K,L) = U_0(K,L)*AIRM(L)
            VM(K,L) = V_0(K,L)*AIRM(L)
         ENDDO
      ENDDO
      ETAL(LM)=ETAL(LM-1)
c      PLE(LM+1)=PTOP+(PSF-PTOP)*SIGE(LM+1)
      PLE(LM+1)=PEDN(LM+1,I,J)
C**** CAL. HEIGHT OF CONDENSATION LEVEL
C     LHX=LHE
C     IF(TL(1).LT.TF) LHX=LHS
C     BXQSAT=LHX*BXCONS
C     AXQSAT=AXCONS+BXQSAT*BYTF
C     TCOND=BXQSAT/(AXQSAT-LOG(1.608*PL(1)*QL(1)+1.D-20))
C     ZCONDL=1000.*(TL(1)-TCOND)/9.76
C****
C**** MOIST CONVECTION
C****
C**** CONVECTION OCCURS AT THE LOWEST MOIST CONVECTIVELY UNSTABLE
C**** LEVEL AND CONTINUES UNTIL A STABLE LAYER PAIR IS REACHED.  RE-
C**** EVAPORATION AND PRECIPITATION ARE COMPUTED AT THE END OF THIS
C**** CYCLE.  THE WHOLE PROCESS MAY BE REPEATED FROM A NEW LOWEST
C**** UNSTABLE LEVEL.
C****
      LMCMIN=0
      LMCMAX=0
      MCCONT=0
C**** OUTER MC LOOP OVER BASE LAYER
      DO 600 LMIN=1,LMCM-1
      MAXLVL=0
      MINLVL=LM
C****
C**** COMPUTE THE CONVECTIVE MASS OF THE NON-ENTRAINING PART
C****
      TERM1=-10.*CK1*SD_CLOUDS(I,J,LMIN+1)/(GRAV*DXYP(J))
      FMP0=TERM1*XMASS
      IF(FMP0.LE.0.) FMP0=0.
C**** CREATE A PLUME IN THE BOTTOM LAYER
C****
C**** ITERATION TO FIND FPLUME WHICH RESTORES THE ATM TO NEUTRAL STATE
C****
      SMO1=SM(LMIN)
      QMO1=QM(LMIN)
      SMO2=SM(LMIN+1)
      QMO2=QM(LMIN+1)
      SDN=SMO1*BYAM(LMIN)
      SUP=SMO2*BYAM(LMIN+1)
      SEDGE=THBAR(SUP,SDN)
      QDN=QMO1*BYAM(LMIN)
      QUP=QMO2*BYAM(LMIN+1)
      WMDN=WM(I,J,LMIN)
      WMUP=WM(I,J,LMIN+1)
      SVDN=SDN*(1.+DELTX*QDN-WMDN)
      SVUP=SUP*(1.+DELTX*QUP-WMUP)
      QEDGE=.5*(QUP+QDN)
      WMEDG=.5*(WMUP+WMDN)
      SVEDG=SEDGE*(1.+DELTX*QEDGE-WMEDG)
      LHX=LHE
      SLH=LHX*BYSHA
      DMSE=(SVUP-SVEDG)*PLK(LMIN+1)+(SVEDG-SVDN)*PLK(LMIN)+
     *  SLHE*(QSAT(SUP*PLK(LMIN+1),LHX,PL(LMIN+1))-QDN)
      IF(DMSE.GT.-1.E-10) GO TO 600
C****
      FPLUME=.25
      DFP = .25
      DO ITER=1,8
      DFP=DFP*0.5
      FMP2=FPLUME*AIRM(LMIN)
      FRAT1=FMP2*BYAM(LMIN+1)
      FRAT2=FMP2*BYAM(LMIN+2)
      SMN1=SMO1*(1.-FPLUME)+FRAT1*SMO2
      QMN1=QMO1*(1.-FPLUME)+FRAT1*QMO2
      SMN2=SMO2*(1.-FRAT1)+FRAT2*SM(LMIN+2)
      QMN2=QMO2*(1.-FRAT1)+FRAT2*QM(LMIN+2)
      SMP=SMO1*FPLUME
      QMP=QMO1*FPLUME
      TP=SMO1*PLK(LMIN+1)*BYAM(LMIN)
      QSATMP=FMP2*QSAT(TP,LHX,PL(LMIN+1))
      GAMA=SLH*LHX*BXCONS*QSATMP/(TP*TP*FMP2)
      DQSUM=(QMP-QSATMP)/(1.+GAMA)
      IF(DQSUM.LE.0.) GO TO 205
      FEVAP=.5*FPLUME
      MCLOUD=FEVAP*AIRM(LMIN+1)
      TNX=SMO2*PLK(LMIN+1)*BYAM(LMIN+1)
      QNX=QMO2*BYAM(LMIN+1)
      QSATC=QSAT(TNX,LHX,PL(LMIN+1))
      DQ=MCLOUD*(QSATC-QNX)/(1.+SLH*LHX*BXCONS*QSATC/(TNX*TNX))
      IF(DQ.GT.DQSUM) DQ=DQSUM
      SMN2=SMN2-SLH*DQ/PLK(LMIN+1)
      QMN2=QMN2+DQ
      DQSUM=DQSUM-DQ
      IF(DQSUM.LE.0.) GO TO 205
      MCLOUD=FEVAP*AIRM(LMIN)
      TNX=SMO1*PLK(LMIN)*BYAM(LMIN)
      QNX=QMO1*BYAM(LMIN)
      QSATC=QSAT(TNX,LHX,PL(LMIN))
      DQ=MCLOUD*(QSATC-QNX)/(1.+SLH*LHX*BXCONS*QSATC/(TNX*TNX))
      IF(DQ.GT.DQSUM) DQ=DQSUM
      SMN1=SMN1-SLH*DQ/PLK(LMIN)
      QMN1=QMN1+DQ
  205 SDN=SMN1*BYAM(LMIN)
      SUP=SMN2*BYAM(LMIN+1)
      SEDGE=THBAR(SUP,SDN)
      QDN=QMN1*BYAM(LMIN)
      QUP=QMN2*BYAM(LMIN+1)
      SVDN=SDN*(1.+DELTX*QDN-WMDN)
      SVUP=SUP*(1.+DELTX*QUP-WMUP)
      QEDGE=.5*(QUP+QDN)
      SVEDG=SEDGE*(1.+DELTX*QEDGE-WMEDG)
      DMSE1=(SVUP-SVEDG)*PLK(LMIN+1)+(SVEDG-SVDN)*PLK(LMIN)+
     *  SLHE*(QSAT(SUP*PLK(LMIN+1),LHX,PL(LMIN+1))-QDN)
      IF (ABS(DMSE1).LE.1.d-3) GO TO 411
      IF(DMSE1.GT.1.d-3) FPLUME=FPLUME-DFP
      IF(DMSE1.LT.-1.d-3) FPLUME=FPLUME+DFP
      END DO
  411 IF(FPLUME.LE..001) GO TO 600
C****
C**** ITERATION THROUGH CLOUD TYPES
C****
      ITYPE=2                        ! always 2 types of clouds:
C     IF(LMIN.LE.2) ITYPE=2          ! entraining & non-entraining
      FTYPE=1.
C**** SET PROFILE TO BE CONSTANT FOR BOTH TYPES OF CLOUDS
      DO L=1,10*LM
        QMOLDALL(L) = QMALL(L)
        SMOLDALL(L) = SMALL(L)
      END DO
      DO 570 IC=1,ITYPE
C**** INITIALLISE VARIABLES USED FOR EACH TYPE
      DO L=1,LM
        COND(L)=0.
        CDHEAT(L)=0.
        DM(L)=0.
        DMR(L)=0.
      END DO
      DUM(1:KMAX,:)=0.
      DVM(1:KMAX,:)=0.
      DO L=1,40*LM
        DMALL(L) = 0.
      END DO
      MC1=.FALSE.
      LHX=LHE
      MPLUME=MIN(1.*AIRM(LMIN),1.*AIRM(LMIN+1))
      IF(MPLUME.GT.FMP2) MPLUME=FMP2
      IF(ITYPE.EQ.2) THEN
      FTYPE=1.
      IF(MPLUME.GT.FMP0) FTYPE=FMP0/MPLUME
      IF(IC.EQ.2) FTYPE=1.-FTYPE
      IF(FTYPE.LT.0.001) GO TO 570
      END IF
      MPLUME=MPLUME*FTYPE
C     FPLUM0=FMP1*BYAM(LMIN)
      FPLUME=MPLUME*BYAM(LMIN)
      SMP  =  SMOLD(LMIN)*FPLUME
      SXMP = SXMOLD(LMIN)*FPLUME
      SYMP = SYMOLD(LMIN)*FPLUME
      SXXMP=SXXMOLD(LMIN)*FPLUME
      SYYMP=SYYMOLD(LMIN)*FPLUME
      SXYMP=SXYMOLD(LMIN)*FPLUME
      QMP  =  QMOLD(LMIN)*FPLUME
      QXMP = QXMOLD(LMIN)*FPLUME
      QYMP = QYMOLD(LMIN)*FPLUME
      QXXMP=QXXMOLD(LMIN)*FPLUME
      QYYMP=QYYMOLD(LMIN)*FPLUME
      QXYMP=QXYMOLD(LMIN)*FPLUME
      TPSAV(LMIN)=SMP*PLK(LMIN)/MPLUME
      DMR(LMIN)=-MPLUME
        DSMR(LMIN)=-SMP
       DSXMR(LMIN)=-SXMP
       DSYMR(LMIN)=-SYMP
      DSXXMR(LMIN)=-SXXMP
      DSYYMR(LMIN)=-SYYMP

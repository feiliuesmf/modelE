c      SUMOLD=0.
c      SUMNEW=0.
c      SUMDP=0.
c      DO 382 L=LMIN,LMAX
c      SUMDP=SUMDP+AIRM(L)
c      SUMOLD=SUMOLD+SM1(L)*PLK(L)

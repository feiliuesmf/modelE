      SUMAJ=SUMAJ+AJ13(L)
  605 CONTINUE
      DO 606 L=LMCMIN,LMCMAX
         AJ13(L)=AJ13(L)-SUMAJ*AIRM(L)/SUMDP
  606 SM(L)=SM(L)-SUMAJ*AIRM(L)/(SUMDP*PLK(L))

         IHOUR=1.5+TOFDAY
      DO 710 J=1,JM

      KMAX=KMAXJ(J)
      IMAX=IMAXJ(J)
C****
C**** MAIN I LOOP
C****
  110 IM1=IM
      DO 700 I=1,IMAX
         JR=JREG(I,J)

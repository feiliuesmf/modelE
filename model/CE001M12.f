      IF (.NOT.MCSSC) SVWMX(I,J,L)=0.
      END IF
        Q(I,J,L)=  QM(L)*BYAM(L)
       QX(I,J,L)= QXM(L)*BYAM(L)
       QY(I,J,L)= QYM(L)*BYAM(L)

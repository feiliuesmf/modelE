        QXYM(L) = QXYM(L) + DQXYM(L)
        QYYM(L) = QYYM(L) + DQYYM(L)
         QZM(L) =  QZM(L) +  DQZM(L)
        QYZM(L) = QYZM(L) + DQYZM(L)
